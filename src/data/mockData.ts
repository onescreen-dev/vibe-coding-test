// Helper functions
export function getArticlesByKeyword(keyword: string): Article[] {
  const keywordData = keywords.find(k => k.text.toLowerCase() === keyword.toLowerCase());
  if (!keywordData) return [];
  return articles.filter(article => keywordData.articles.includes(article.id));
}

export function getKeywordsByFilter(continent?: string, region?: string, category?: string): Keyword[] {
  return keywords.filter(keyword => {
    if (continent && continent !== 'global' && !keyword.continents.includes(continent)) return false;
    if (region && !keyword.regions.includes(region)) return false;
    if (category && keyword.category !== category) return false;
    return true;
  });
}

export function searchKeywords(query: string): Keyword[] {
  const lowerQuery = query.toLowerCase();
  return keywords.filter(keyword =>
    keyword.text.toLowerCase().includes(lowerQuery)
  );
}

export function getTopCategories(limit: number = 30): Category[] {
  return categories
    .sort((a, b) => b.count - a.count)
    .slice(0, limit);
}
